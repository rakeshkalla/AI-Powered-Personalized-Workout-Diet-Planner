def recommend_diet(goal):

    if goal == "Weight Loss":

        return [
            "Oats",
            "Apple",
            "Salad",
            "Brown Rice",
            "Green Tea"
        ]

    elif goal == "Muscle Gain":

        return [
            "Eggs",
            "Chicken Breast",
            "Milk",
            "Paneer",
            "Banana"
        ]

    else:

        return [
            "Balanced Diet",
            "Fruits",
            "Vegetables",
            "Whole Grains"
        ]